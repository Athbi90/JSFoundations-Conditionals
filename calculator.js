let x = 9;
let y = 5;
let operation = "Subtraction";

if(operation === "Addition"){
    console.log(`${x} + ${y} = ${x+y}`);
} else if(operation === "Subtraction"){
    console.log(`${x} - ${y} = ${x-y}`); 
} else if(operation === "Multiplication"){
    console.log(`${x} * ${y} = ${x*y}`); 
} else if(operation === "Division"){
    console.log(`${x} / ${y} = ${x/y}`); 
}



